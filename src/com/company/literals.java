package com.company;
public class literals {
    public static void main(String[] args) {
        byte age = 20; // using byte case age can be max up to 127 which is range of byte
        short age1 = 40;
        float f1 = 10.6f; //for float, it is compulsory to use f as by default it is double
        double d1 = 10.7; //just like float, for double I can use d but not compulsory
        long ageOfDino = 100000l; //we add l cause by default it is considered as integer
        char ch = 'A';
        String name = "Abdullah";
        System.out.println("The age is: " + age); //press right run and run else the main file would be run
        System.out.println("My name is: " + name);
    }
}

// All the things 20,10.6f,'A',10.7,100000l are all literals